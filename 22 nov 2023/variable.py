# Define variables with various data types
integer_var = 10
string_var = "Hello, Python!"
list_var = [1, 2, 3, 4, 5]
tuple_var = (1, "apple", 3.14)
dict_var = {"name": "John", "age": 25}

# Print each variable along with its data type
print("Integer Variable:", integer_var, type(integer_var))
print("String Variable:", string_var, type(string_var))
print("List Variable:", list_var, type(list_var))
print("Tuple Variable:", tuple_var, type(tuple_var))
print("Dictionary Variable:", dict_var, type(dict_var))
